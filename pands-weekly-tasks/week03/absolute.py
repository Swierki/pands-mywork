# absolute.py
# that takes in number and give its absolute value
# Author: Lukasz Swierkowski
# it needs float() so the string is converted into a decimal number.

# Get input from the user
number = float(input("Enter a number: "))
        
# Calculate absolute value
abs_value = abs(number)
        
# Print the result
print(f"The absolute value of {number} is {abs_value}")
